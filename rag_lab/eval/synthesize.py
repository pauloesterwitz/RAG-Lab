"""Synthesize the evaluation 'goldens' (gold chunks → question + reference
answer) with DeepEval's Synthesizer, judged/generated by the local Ollama model.

Each golden's `context` is the gold chunk(s) it was generated from — that is the
gold-chunk ground truth the retrieval metrics are scored against."""
from __future__ import annotations

import hashlib
import json
import random
from collections import defaultdict
from pathlib import Path

import numpy as np

from deepeval.synthesizer import Synthesizer
from deepeval.synthesizer.config import StylingConfig, FiltrationConfig

from ..config import SETTINGS, EVAL_DIR
from ..indexer import load_base_index
from ..store import BaseIndex
from .deepeval_models import get_judge

GOLDENS_FILE = EVAL_DIR / "goldens.json"

_STYLING = StylingConfig(
    scenario=(
        "A technical reader studying a corpus of AI/ML and software-engineering "
        "research papers and book chapters, asking focused factual questions."
    ),
    task=(
        "Answer specific questions grounded in the source passages about concepts, "
        "methods, definitions, trade-offs and findings."
    ),
    input_format="A clear, self-contained question answerable from the passage.",
    expected_output_format="A concise, factual answer grounded only in the passage.",
)


def _ctx_key(ctx: list[str]) -> str:
    return hashlib.sha1("||".join(ctx).encode()).hexdigest()[:16]


def _select_contexts(index: BaseIndex, n: int) -> tuple[list[list[str]], list[str], dict]:
    """Round-robin across documents; each context = a chunk + its same-doc neighbour.
    Returns (contexts, source_files, ctx_key -> [chunk_ids])."""
    by_doc: dict[str, list[int]] = defaultdict(list)
    for i, c in enumerate(index.chunks):
        if c.char_count >= 600:  # skip sparse/figure chunks
            by_doc[c.doc].append(i)
    rng = random.Random(42)
    for lst in by_doc.values():
        rng.shuffle(lst)

    contexts: list[list[str]] = []
    sources: list[str] = []
    key_to_ids: dict[str, list[str]] = {}
    docs = list(by_doc.keys())
    pos = {d: 0 for d in docs}
    while len(contexts) < n:
        progressed = False
        for d in docs:
            lst = by_doc[d]
            if pos[d] >= len(lst):
                continue
            i = lst[pos[d]]
            pos[d] += 1
            progressed = True
            chunk = index.chunks[i]
            ids = [chunk.id]
            texts = [chunk.text]
            # add same-doc neighbour (next chunk index in original order if same doc)
            if i + 1 < len(index.chunks) and index.chunks[i + 1].doc == chunk.doc:
                texts.append(index.chunks[i + 1].text)
                ids.append(index.chunks[i + 1].id)
            contexts.append(texts)
            sources.append(chunk.doc)
            key_to_ids[_ctx_key(texts)] = ids
            if len(contexts) >= n:
                break
        if not progressed:
            break
    return contexts, sources, key_to_ids


def _select_multihop_contexts(index: BaseIndex, n: int) -> tuple[list[list[str]], list[str], dict]:
    """Cross-document contexts: pair each seed chunk with its most semantically
    similar chunk in a DIFFERENT document. A question synthesized from the pair
    needs to combine both sources — exactly where single-shot plain retrieval
    struggles and reranking / agentic / graph should help."""
    emb = index.embeddings  # already L2-normalized (N, d)
    eligible = [i for i, c in enumerate(index.chunks) if c.char_count >= 600]
    rng = random.Random(43)
    rng.shuffle(eligible)

    contexts: list[list[str]] = []
    sources: list[str] = []
    key_to_ids: dict[str, list[str]] = {}
    used: set[int] = set()
    for si in eligible:
        if len(contexts) >= n:
            break
        if si in used:
            continue
        seed_doc = index.chunks[si].doc
        sims = emb @ emb[si]
        partner = None
        for j in np.argsort(-sims):
            j = int(j)
            if j != si and j not in used and index.chunks[j].doc != seed_doc and index.chunks[j].char_count >= 600:
                partner = j
                break
        if partner is None:
            continue
        used.add(si)
        used.add(partner)
        texts = [index.chunks[si].text, index.chunks[partner].text]
        ids = [index.chunks[si].id, index.chunks[partner].id]
        contexts.append(texts)
        sources.append(f"{seed_doc} + {index.chunks[partner].doc}")
        key_to_ids[_ctx_key(texts)] = ids
    return contexts, sources, key_to_ids


def synthesize_goldens(num: int | None = None, multihop_frac: float = 0.5, progress=None) -> dict:
    num = num or SETTINGS.eval_num_goldens
    index = load_base_index(refresh=True)
    n_multi = int(round(num * multihop_frac))
    n_single = num - n_multi
    # oversample (filtration drops some)
    s_ctx, s_src, s_keys = _select_contexts(index, int(n_single * 1.4) + 2)
    m_ctx, m_src, m_keys = _select_multihop_contexts(index, int(n_multi * 1.4) + 2)
    key_hop = {k: "single" for k in s_keys} | {k: "multi" for k in m_keys}
    key_to_ids = {**s_keys, **m_keys}
    contexts = s_ctx + m_ctx
    sources = s_src + m_src
    if progress:
        progress(f"Synthesizing {n_single} single-hop + {n_multi} multi-hop from "
                 f"{len(contexts)} contexts using {SETTINGS.judge_model}…")

    synth = Synthesizer(
        model=get_judge(),
        async_mode=True,
        # Claude: serialize — the shared org rate limiter makes concurrency bursty.
        max_concurrent=1 if SETTINGS.provider == "claude" else SETTINGS.gen_concurrency,
        styling_config=_STYLING,
        filtration_config=FiltrationConfig(
            critic_model=get_judge(), synthetic_input_quality_threshold=0.5, max_quality_retries=1
        ),
    )
    goldens = synth.generate_goldens_from_contexts(
        contexts=contexts,
        include_expected_output=True,
        max_goldens_per_context=1,
        source_files=sources,
    )

    out = []
    for g in goldens:
        ctx = list(g.context or [])
        k = _ctx_key(ctx)
        out.append(
            {
                "input": g.input,
                "expected_output": g.expected_output,
                "context": ctx,
                "source_file": g.source_file,
                "hop": key_hop.get(k, "single"),
                "gold_chunk_ids": key_to_ids.get(k, []),
            }
        )
    # keep a balanced cut near `num`
    singles = [o for o in out if o["hop"] == "single"][:n_single]
    multis = [o for o in out if o["hop"] == "multi"][:n_multi]
    out = singles + multis
    payload = {
        "judge_model": SETTINGS.judge_model,
        "num_goldens": len(out),
        "n_single": len(singles),
        "n_multi": len(multis),
        "goldens": out,
    }
    GOLDENS_FILE.write_text(json.dumps(payload, indent=2))
    if progress:
        progress(f"Synthesized {len(out)} goldens ({len(singles)} single, {len(multis)} multi) → {GOLDENS_FILE.name}")
    return payload


def load_goldens() -> dict | None:
    if GOLDENS_FILE.exists():
        return json.loads(GOLDENS_FILE.read_text())
    return None
